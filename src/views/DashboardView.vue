<script setup>
  import { ref, computed, watch, onMounted } from 'vue';
  import { useDataStore } from '@/stores/dataStore.js';

  const dataStore = useDataStore();

  const activeLayerTab = ref(null); /** 📑 當前作用中的圖層分頁 */

  // 獲取所有開啟且有資料的圖層
  const visibleLayers = computed(() => {
    const allLayers = dataStore.getAllLayers();
    return allLayers.filter((layer) => layer.visible);
  });

  /**
   * 📑 設定作用中圖層分頁 (Set Active Layer Tab)
   * @param {string} layerId - 圖層 ID
   */
  const setActiveLayerTab = (layerId) => {
    activeLayerTab.value = layerId;
  };

  /**
   * 📊 當前圖層摘要 (Current Layer Summary)
   */
  const currentLayerSummary = computed(() => {
    if (!activeLayerTab.value) return null;
    const layer = visibleLayers.value.find((l) => l.id === activeLayerTab.value);
    return layer ? layer.summary || null : null;
  });

  /**
   * 📊 取得當前選中圖層名稱 (Get Current Selected Layer Name)
   */
  const currentLayerName = computed(() => {
    if (!activeLayerTab.value) return '無開啟圖層';
    const layer = visibleLayers.value.find((l) => l.id === activeLayerTab.value);
    return layer ? layer.name || '未知圖層' : '無開啟圖層';
  });

  /**
   * 📊 計算圖層統計數據 (Calculate Layer Statistics)
   * @param {Object} layer - 圖層物件
   */
  const getLayerStats = (layer) => {
    const data = layer.tableData || [];
    if (data.length === 0) {
      return {
        total: 0,
        max: 0,
        average: 0,
        hasDataCount: 0,
      };
    }

    // 計算統計數據
    const counts = data.map((item) => {
      // 動態獲取數值欄位
      const numericFields = Object.keys(item).filter((key) => {
        const value = item[key];
        return typeof value === 'number' && !isNaN(value);
      });

      // 優先使用常見的數值欄位名稱
      const priorityFields = ['count', 'value', 'amount', 'population', 'density'];
      const field = priorityFields.find((f) => numericFields.includes(f)) || numericFields[0];

      return field ? item[field] || 0 : 0;
    });

    const validCounts = counts.filter((c) => c > 0);
    const total = data.length;
    const max = counts.length > 0 ? Math.max(...counts) : 0;
    const average = counts.length > 0 ? counts.reduce((a, b) => a + b, 0) / counts.length : 0;
    const hasDataCount = validCounts.length;

    return {
      total,
      max,
      average,
      hasDataCount,
    };
  };

  // /**
  //  * 📊 當前圖層統計數據 (Current Layer Statistics)
  //  */
  // const currentLayerStats = computed(() => {
  //   if (!activeLayerTab.value) return { total: 0, max: 0, average: 0, hasDataCount: 0 };
  //   const layer = visibleLayers.value.find((l) => l.id === activeLayerTab.value);
  //   return layer ? getLayerStats(layer) : { total: 0, max: 0, average: 0, hasDataCount: 0 };
  // });

  // 記錄上一次的圖層列表用於比較
  const previousLayers = ref([]);

  /**
   * 👀 監聽可見圖層變化，自動切換到新開啟的圖層分頁
   */
  watch(
    () => visibleLayers.value,
    (newLayers) => {
      // 如果沒有可見圖層，清除選中的分頁
      if (newLayers.length === 0) {
        activeLayerTab.value = null;
        previousLayers.value = [];
        return;
      }

      // 找出新增的圖層（比較新舊圖層列表）
      const previousLayerIds = previousLayers.value.map((layer) => layer.id);
      const newLayerIds = newLayers.map((layer) => layer.id);
      const addedLayerIds = newLayerIds.filter((id) => !previousLayerIds.includes(id));

      // 如果有新增的圖層，自動切換到最新新增的圖層
      if (addedLayerIds.length > 0) {
        const newestAddedLayerId = addedLayerIds[addedLayerIds.length - 1];
        activeLayerTab.value = newestAddedLayerId;
        console.log(
          `🔄 自動切換到新開啟的圖層: ${newLayers.find((layer) => layer.id === newestAddedLayerId)?.name}`
        );
      }
      // 如果當前沒有選中分頁，或選中的分頁不在可見列表中，選中第一個
      else if (
        !activeLayerTab.value ||
        !newLayers.find((layer) => layer.id === activeLayerTab.value)
      ) {
        activeLayerTab.value = newLayers[0].id;
      }

      // 更新記錄的圖層列表
      previousLayers.value = [...newLayers];
    },
    { deep: true, immediate: true }
  );

  /**
   * 🚀 組件掛載事件 (Component Mounted Event)
   */
  onMounted(() => {
    console.log('[DashboardView] Component Mounted');

    // 初始化第一個可見圖層為作用中分頁
    if (visibleLayers.value.length > 0 && !activeLayerTab.value) {
      activeLayerTab.value = visibleLayers.value[0].id;
    }
  });
</script>

<template>
  <!-- 📊 多圖層數據儀表板視圖組件 -->
  <div class="h-100 dashboard-container bg-light">
    <div class="d-flex flex-column h-100">
      <!-- 📑 圖層分頁導航 -->
      <div v-if="visibleLayers.length > 0" class="border-bottom bg-white">
        <ul class="nav nav-tabs nav-tabs-custom mb-0">
          <li v-for="layer in visibleLayers" :key="layer.id" class="nav-item">
            <button
              class="nav-link d-flex align-items-center gap-2"
              :class="{ active: activeLayerTab === layer.id }"
              @click="setActiveLayerTab(layer.id)"
            >
              <i class="fas fa-layer-group"></i>
              <span>{{ layer.name }}</span>
              <span class="badge bg-primary ms-1">
                {{ getLayerStats(layer).total }}
              </span>
            </button>
          </li>
        </ul>
      </div>

      <!-- 📊 圖層儀表板內容 -->
      <div class="flex-grow-1 overflow-auto">
        <!-- 有開啟圖層時的內容 -->
        <div v-if="visibleLayers.length > 0 && activeLayerTab" class="p-4">
          <!-- 📊 當前圖層資訊 -->
          <div class="mb-4">
            <div class="card">
              <div class="card-header bg-primary text-white">
                <h5 class="mb-0">
                  <i class="fas fa-layer-group"></i>
                  {{ currentLayerName }}
                </h5>
              </div>
            </div>
          </div>

          <!-- 📊 圖層摘要資料 -->
          <div class="row flex-grow-1">
            <div class="col-12">
              <div class="card h-100">
                <div class="card-header">
                  <h6 class="mb-0"><i class="fas fa-info-circle"></i> 圖層摘要資料</h6>
                </div>
                <div class="card-body">
                  <div v-if="currentLayerSummary" class="summary-content">
                    <pre class="json-display bg-light p-3 rounded">{{
                      JSON.stringify(currentLayerSummary, null, 2)
                    }}</pre>
                  </div>
                  <div v-else class="text-center text-muted py-5">
                    <i class="fas fa-exclamation-triangle fa-3x mb-3"></i>
                    <h5>沒有摘要資料</h5>
                    <p>此圖層沒有可用的摘要資訊</p>
                  </div>
                </div>
              </div>
            </div>
          </div>
        </div>

        <!-- 沒有開啟圖層時的空狀態 -->
        <div
          v-else
          class="d-flex flex-column justify-content-center align-items-center h-100 text-muted"
        >
          <i class="fas fa-layer-group fa-4x mb-4 opacity-50"></i>
          <h4>沒有開啟的圖層</h4>
          <p class="text-center">請開啟至少一個圖層以查看儀表板統計資訊</p>
        </div>
      </div>
    </div>
  </div>
</template>

