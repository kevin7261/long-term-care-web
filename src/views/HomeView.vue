        // ÂàáÊèõÂà∞Ë°®Ê†ºtab
        activeBottomTab.value = 'table'
        
        console.log('‚úÖ Âè∞ÂçóÊï∏ÊìöËºâÂÖ•ÂÆåÊàê:', data.summary)
      } catch (error) {
        console.error('‚ùå ËºâÂÖ•Âè∞ÂçóÊï∏ÊìöÂ§±Êïó:', error)
        alert('ËºâÂÖ•Êï∏ÊìöÂ§±ÊïóÔºåË´ãÊ™¢Êü•Êñá‰ª∂Ë∑ØÂæëÂíåÊ†ºÂºè')
      } finally {
        isLoadingData.value = false
        isLoading.value = false
        loadingProgress.value = 0
        showLoadingProgress.value = false
      }
    }

    /**
     * üóëÔ∏è Ê∏ÖÈô§Âè∞ÂçóÊï∏Êìö (Clear Tainan Data)
     */
    const clearTainanData = () => {
      if (confirm('Á¢∫ÂÆöË¶ÅÊ∏ÖÈô§Âè∞ÂçóÊï∏ÊìöÂóéÔºü')) {
        // Ê∏ÖÈô§Êï∏Êìö
        tainanDataSummary.value = null
        mergedTableData.value = []
        tainanGeoJSONData.value = null
        tableSearchQuery.value = ''
        
        // Èö±ËóèÂúñÂ±§
        showTainanLayer.value = false
        
        console.log('‚úÖ Âè∞ÂçóÊï∏ÊìöÂ∑≤Ê∏ÖÈô§')
      }
    }

    // üó∫Ô∏è Âú∞Âúñ‰∫íÂãïÂáΩÊï∏ (Map Interaction Functions)
    
    /**
     * üéØ Âú®Âú∞Âúñ‰∏äÈ´ò‰∫ÆÈ°ØÁ§∫ (Highlight on Map)
     */
    const highlightOnMap = (row) => {
      if (mainContent.value) {
        mainContent.value.highlightFeature(row.code2)
      }
    }

    /**
     * üó∫Ô∏è ÈÅ©ÊáâÂú∞ÂúñÂà∞Êï∏ÊìöÁØÑÂúç (Fit Map to Data)
