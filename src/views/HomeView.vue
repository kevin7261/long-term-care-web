    const switchToDashboard = () => {
      activeTab.value = 'dashboard'
    }

    /**
     * üïê ÂèñÂæóÁï∂ÂâçÊôÇÈñì (Get Current Time)
     */
    const getCurrentTime = () => {
      return new Date().toLocaleString('zh-TW')
