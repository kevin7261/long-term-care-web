    // üîß ÊãñÊãΩË™øÊï¥ÂäüËÉΩ (Drag Resize Functions)
    
    /**
     * üîß ÈñãÂßãË™øÊï¥Èù¢ÊùøÂ§ßÂ∞è (Start Panel Resize)
     * ÊîπÈÄ≤ÁâàÊú¨ÁöÑÊãñÊõ≥Á≥ªÁµ±ÔºåÊèê‰æõÊõ¥ÊµÅÊö¢ÁöÑÈ´îÈ©ó
     */
    const startResize = (direction, event) => {
      event.preventDefault()
      event.stopPropagation()
      
      isDragging.value = true
      document.body.classList.add('my-no-select')
      
      // Ë®òÈåÑÂàùÂßã‰ΩçÁΩÆÂíåÈù¢ÊùøÂ∞∫ÂØ∏
      const startX = event.clientX
      const startY = event.clientY
      const startLeftWidth = leftPanelWidth.value
      const startRightWidth = rightPanelWidth.value
      const startBottomHeight = bottomPanelHeight.value
      
      // Áç≤ÂèñÁ™óÂè£Â∞∫ÂØ∏‰ª•Ë®àÁÆóÁôæÂàÜÊØî
      const windowWidth = window.innerWidth
      
      console.log(`üîß ÈñãÂßãË™øÊï¥ ${direction} ÊñπÂêëÔºåÂàùÂßãÂÄº:`, {
        leftWidth: startLeftWidth,
        rightWidth: startRightWidth,
        bottomHeight: startBottomHeight
      })

      const handleMouseMove = (moveEvent) => {
        moveEvent.preventDefault()
        
        const deltaX = moveEvent.clientX - startX
        const deltaY = moveEvent.clientY - startY
        
        // Ë®àÁÆóÁôæÂàÜÊØîËÆäÂåñÈáè
        const deltaXPercent = (deltaX / windowWidth) * 100
        
        if (direction === 'left') {
          // Ë™øÊï¥Â∑¶ÂÅ¥Èù¢ÊùøÂØ¨Â∫¶ (10-100%) - ‰øÆÂæ©ÊãñÊãΩÊñπÂêëÔºåÂêëÂè≥ÊãñÊãΩÂ¢ûÂä†ÂØ¨Â∫¶
          const newWidth = Math.max(10, Math.min(100, startLeftWidth + deltaXPercent))
          leftPanelWidth.value = newWidth
        } else if (direction === 'right') {
          // Ë™øÊï¥Âè≥ÂÅ¥Èù¢ÊùøÂØ¨Â∫¶ (10-100%) - ÂêëÂ∑¶ÊãñÊãΩÂ¢ûÂä†ÂØ¨Â∫¶ÔºåÂêëÂè≥ÊãñÊãΩÊ∏õÂ∞ëÂØ¨Â∫¶
          const newWidth = Math.max(10, Math.min(100, startRightWidth - deltaXPercent))
          rightPanelWidth.value = newWidth
        } else if (direction === 'bottom') {
          // Ë™øÊï¥Â∫ïÈÉ®Èù¢ÊùøÈ´òÂ∫¶ (10-100%) - ‰øÆÂæ©ÊãñÊãΩÈÇèËºØÔºåÁ¢∫‰øùÂèØÂà∞100%
          const viewportHeight = windowHeight.value - 116 // Êâ£Èô§headerÂíåfooter
          const deltaYPercent = (deltaY / viewportHeight) * 100
          const currentHeightPercent = (startBottomHeight / viewportHeight) * 100
          const newHeightPercent = Math.max(10, Math.min(100, currentHeightPercent - deltaYPercent))
          bottomPanelHeight.value = (newHeightPercent / 100) * viewportHeight
        }
      }

      const handleMouseUp = () => {
        isDragging.value = false
        document.body.classList.remove('my-no-select')
        document.removeEventListener('mousemove', handleMouseMove)
        document.removeEventListener('mouseup', handleMouseUp)
        
        // È©óË≠âÊúÄÁµÇÂ∞∫ÂØ∏
        validatePanelSizes()
        
        console.log('‚úÖ ÊãñÊõ≥Ë™øÊï¥ÂÆåÊàêÔºåÊúÄÁµÇÂÄº:', {
          leftWidth: leftPanelWidth.value,
          rightWidth: rightPanelWidth.value,
          bottomHeight: bottomPanelHeight.value,
          mainWidth: mainPanelWidth.value
        })
      }

      document.addEventListener('mousemove', handleMouseMove)
      document.addEventListener('mouseup', handleMouseUp)
    }
    
    /**
     * ‚úÖ È©óË≠âÈù¢ÊùøÂ∞∫ÂØ∏ (Validate Panel Sizes)
     * Á¢∫‰øùÈù¢ÊùøÂ∞∫ÂØ∏Âú®ÂêàÁêÜÁØÑÂúçÂÖß (10-100%)
     */
    const validatePanelSizes = () => {
      // Á¢∫‰øùÂêÑÈù¢ÊùøÂú®10-100%ÁØÑÂúçÂÖß
      leftPanelWidth.value = Math.max(10, Math.min(100, leftPanelWidth.value))
      rightPanelWidth.value = Math.max(10, Math.min(100, rightPanelWidth.value))
      
      // Â∫ïÈÉ®Èù¢ÊùøÈ´òÂ∫¶ÈôêÂà∂ (10-100%)
      if (bottomPanelHeight.value !== null) {
        const viewportHeight = windowHeight.value - 116 // Êâ£Èô§headerÂíåfooter
        const minHeight = (10 / 100) * viewportHeight
        const maxHeight = (100 / 100) * viewportHeight
        bottomPanelHeight.value = Math.max(minHeight, Math.min(maxHeight, bottomPanelHeight.value))
      }
      
      // ÂõõÊç®‰∫îÂÖ•Âà∞‰∏Ä‰ΩçÂ∞èÊï∏
      leftPanelWidth.value = Math.round(leftPanelWidth.value * 10) / 10
      rightPanelWidth.value = Math.round(rightPanelWidth.value * 10) / 10
    }

    // üìè Ë¶ñÁ™óÂ§ßÂ∞èËÆäÂåñËôïÁêÜ (Window Resize Handler)
    const handleWindowResize = () => {
      windowWidth.value = window.innerWidth
      windowHeight.value = window.innerHeight
      
      // Ë¶ñÁ™óÂ§ßÂ∞èËÆäÂåñÊôÇÈáçÊñ∞È©óË≠âÈù¢ÊùøÂ∞∫ÂØ∏
      validatePanelSizes()
    }

    /**
     * üöÄ ÁµÑ‰ª∂ÊéõËºâ (Component Mounted)
     */
    onMounted(() => {
      // ÂàùÂßãÂåñÂ∫ïÈÉ®Èù¢ÊùøÈ´òÂ∫¶ÁÇ∫30%
      const viewportHeight = windowHeight.value - 116 // Êâ£Èô§headerÂíåfooter
      bottomPanelHeight.value = (30 / 100) * viewportHeight
      
      window.addEventListener('resize', handleWindowResize)
      console.log('üöÄ Á©∫ÈñìÂàÜÊûêÂπ≥Âè∞Â∑≤ÂàùÂßãÂåñ')
    })

    /**
     * üóëÔ∏è ÁµÑ‰ª∂Âç∏Ëºâ (Component Unmounted)
     */
    onUnmounted(() => {
      window.removeEventListener('resize', handleWindowResize)
    })

    // üì§ ËøîÂõûÈüøÊáâÂºèÊï∏ÊìöÂíåÂáΩÊï∏ (Return Reactive Data and Functions)
    return {
      // üìö ÂÖÉ‰ª∂ÂºïÁî®
      mainContent,
      
      // üìë ÂàÜÈ†ÅÁãÄÊÖã
      activeTab,
      activeBottomTab,
      activeRightTab,
      
      // ‚è≥ ËºâÂÖ•ÁãÄÊÖã
      isLoading,
      isLoadingData,
      loadingText,
      loadingProgress,
      showLoadingProgress,
      loadingSubText,
      
      // üó∫Ô∏è ÂúñÂ±§ÁãÄÊÖãÔºàÂ∑≤ÁßªÈô§showLayer1ÂíåshowLayer2Ôºâ
      showTainanLayer,
      selectedFilter,
      selectedColorScheme,
      
      // üó∫Ô∏è Âú∞ÂúñÁãÄÊÖã
      zoomLevel,
      currentCoords,
      
      // üìä Áµ±Ë®àÊï∏Êìö
      totalCount,
      totalDataPoints,
      selectedCount,
      activeMarkers,
      maxCount,
      averageCount,
      dataRegionsCount,
      
      // üìè Èù¢ÊùøÂ∞∫ÂØ∏ÔºàÁôæÂàÜÊØîÁ≥ªÁµ±Ôºâ
      leftPanelWidth,
      rightPanelWidth,
      bottomPanelHeight,
      leftPanelWidthPx,
      rightPanelWidthPx,
      mainPanelWidth,
      mainPanelWidthPx,
      contentHeight,
      
      // üìä Âè∞ÂçóÊï∏Êìö
      tainanDataSummary,
      mergedTableData,
      tableSearchQuery,
      filteredTableData,
      sortedAndFilteredTableData,
      tainanGeoJSONData,
      
      // üìã Ë°®Ê†ºÊéíÂ∫è
      sortField,
      sortDirection,
      
      // üìà ÂàÜÊûêÊï∏Êìö
      analysisList,
      selectedAnalysisId,
      canStartAnalysis,
      
      // üìà ÂàÜÊûêÂäüËÉΩ
      startAnalysis,
      selectAnalysis,
      viewAnalysis,
      deleteAnalysis,
      
      // üì• Âè∞ÂçóÊï∏ÊìöÂäüËÉΩ
      loadTainanData,
      clearTainanData,
      highlightOnMap,
      fitMapToData,
      resetView,
      switchToDashboard,
      
      // üìã Ë°®Ê†ºÂäüËÉΩ
      sortTable,
      
      // üîß ÊãñÊãΩË™øÊï¥ÂäüËÉΩ
      startResize,
      isDragging,
      validatePanelSizes,
      
      // üõ†Ô∏è Â∑•ÂÖ∑ÂáΩÊï∏
      formatNumber,
      getCurrentTime
    }
  }
}
